package com.extjs.gxt.ui.client.util;

public class Margins {

  public int left;
  
  public int right;
  
  public int top;
  
  public int bottom;
  
  public Margins() {
    
  }
  
  public Margins(int top, int right, int bottom, int left) {
    this.top = top;
    this.right = right;
    this.bottom = bottom;
    this.left = left;
  }
  
}
